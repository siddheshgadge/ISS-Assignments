// Basic Datatypes

class Datatypes{
	public static void main(String[] args) {
		// Integer datatypes
		byte a = 1;
		short b = 2;
		int c = 3;
		long d = 100000L;
		// decimal point datatypes
		float e = 23.45f;
		double f = 123.4;
		// boolean datatype
		boolean g = true;
		boolean h = false;
		// Character datatype
		char i = 'a';
		// Datatypes is reference datatype of class 'Datatypes'
		Datatypes referenceDatatypeVar = new Datatypes();
		// Literals Integer can be expressed in terms of decimal octal and hexadecimal format
		int decimal = 100;
		int octal = 0124;
		int hexadecimal = 0x64;
		// String and char types of literals
		char u = '\u0001';
		String x = "\u0001";
	}
}